class Holidays {
  String? holidayDate, key, holidayType;

  String? get getHolidayDate {
    return this.holidayDate;
  }

  void setHolidayDate(String string) {
    holidayDate = string;
  }

  String? get getKey {
    return this.key;
  }

  void setKey(String string) {
    key = string;
  }

  String? get getHolidayType {
    return this.holidayType;
  }

  void setHolidayType(String string) {
    holidayType = string;
  }
}
